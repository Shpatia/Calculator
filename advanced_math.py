import math

def sin_func(x):
    return round(math.sin(math.radians(x)), 6)

def cos_func(x):
    return round(math.cos(math.radians(x)), 6)

def power(a, b):
    return a ** b

def sqrt_func(x):
    return math.sqrt(x)

def floor_func(x):
    return math.floor(x)

def ceil_func(x):
    return math.ceil(x)